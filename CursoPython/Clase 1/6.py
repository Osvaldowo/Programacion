frutas = ["manzana","banana","cereza","naranja"]
print(f"Mi lista de frutas es: {frutas}")

primera_fruta = frutas[0]
print(f"La primera fruta de la lista (indice 0) es: {primera_fruta}")

dos_frutas_del_medio = frutas[1:3]
print(f"Las frutas de media lista (indice 0) es: {dos_frutas_del_medio}")

ultimas_dos_frutas = frutas[2:4]
print(f"Las ultimas frutas de la lista (indice 0) es: {ultimas_dos_frutas}")